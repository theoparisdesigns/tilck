/* Key definitions (partial) for PS/2 scancode set 1 */

#define KEY_L_SHIFT        0x2A
#define KEY_R_SHIFT        0x36
#define KEY_NUM_LOCK       0x45
#define KEY_CAPS_LOCK      0x3A

#define KEY_F1              0x3B
#define KEY_F2      (KEY_F1 + 1)
#define KEY_F3      (KEY_F1 + 2)
#define KEY_F4      (KEY_F1 + 3)
#define KEY_F5      (KEY_F1 + 4)
#define KEY_F6      (KEY_F1 + 5)
#define KEY_F7      (KEY_F1 + 6)
#define KEY_F8      (KEY_F1 + 7)
#define KEY_F9      (KEY_F1 + 8)
#define KEY_F10     (KEY_F1 + 9)
#define KEY_F11             0x57
#define KEY_F12             0x58

#define KEY_UP            0xE048
#define KEY_DOWN          0xE050
#define KEY_RIGHT         0xE04d
#define KEY_LEFT          0xE04b
#define KEY_NUMPAD_UP       0x48
#define KEY_NUMPAD_DOWN     0x50
#define KEY_NUMPAD_RIGHT    0x4d
#define KEY_NUMPAD_LEFT     0x4b

#define KEY_LEFT_CTRL       0x1d
#define KEY_RIGHT_CTRL    0xE01d
#define KEY_LEFT_ALT        0x38
#define KEY_RIGHT_ALT     0xE038

#define KEY_INS           0xE052
#define KEY_NUMPAD_INS      0x52
#define KEY_HOME          0xE047
#define KEY_NUMPAD_HOME     0x47
#define KEY_DEL           0xE053
#define KEY_NUMPAD_DEL      0x53
#define KEY_END           0xE04f
#define KEY_NUMPAD_END      0x4f

#define KEY_PAGE_UP            0xE049
#define KEY_NUMPAD_PAGE_UP       0x49
#define KEY_PAGE_DOWN          0xE051
#define KEY_NUMPAD_PAGE_DOWN     0x51

#define KEY_NUMPAD_ENTER       0xE01c
#define KEY_NUMPAD_SLASH       0xE035
